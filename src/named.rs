use core::fmt;
use once_cell::sync::Lazy;

use crate::Color;

#[derive(Debug, Clone, PartialEq)]
pub enum NamedColorKind {
    CSS,
    X11,
}

impl fmt::Display for NamedColorKind {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            NamedColorKind::CSS => write!(f, "CSS"),
            NamedColorKind::X11 => write!(f, "X11"),
        }
    }
}

impl NamedColorKind {
    pub fn match_names(&self, namekinds: &Option<Vec<&str>>) -> bool {
        match &namekinds {
            None => true,
            Some(namekinds) => {
                namekinds
                    .iter()
                    .any(|namekind| match namekind.to_ascii_uppercase().as_str() {
                        "CSS" => self == &NamedColorKind::CSS,
                        "X11" => self == &NamedColorKind::X11,
                        _ => false,
                    })
            }
        }
    }
}

#[derive(Debug, Clone)]
pub struct NamedColor {
    pub name: &'static str,
    pub color: Color,
    pub kind: NamedColorKind,
}

fn named_color(name: &'static str, r: u8, g: u8, b: u8, kind: NamedColorKind) -> NamedColor {
    NamedColor {
        name,
        color: Color::from_rgb(r, g, b),
        kind,
    }
}

